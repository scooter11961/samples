
package com.aol.functest.common;

/**
*	Class which is generic window. 
*	@version 1.0
*	@author shiladityaroy
*/
public class window extends BrowserPageElement { 

	String sWindow;

	/**
	*	Constructor to define any generic window
	*	@param sWnd Identifier of a window
	*	@return void
	*/
	public window (browserpage page, String sWnd) {
		super(page);
		sWindow = sWnd;
	}

	/**
	 * Method which will return the ID of the object being initialized.
	 * @param none 
	 * @return String - Identifier of the object
	 */
	
	public String getWindowID () {
		return sWindow;
	}
	
}