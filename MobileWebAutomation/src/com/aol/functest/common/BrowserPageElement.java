package com.aol.functest.common;

/**
 * @author freebeachler
 *
 */
public abstract class BrowserPageElement {

	/**
	 * Instantiate the element with the browserpage
	 * it interacts with.
	 * 
	 * @param browserpage
	 */
	public BrowserPageElement(browserpage browserpage) {
		setBrowserPage(browserpage);
	}

	/**
	 * The page that element lives on.
	 */
	private browserpage browserpage;

	/**
	 * @return the browserpage
	 */
	public browserpage getBrowserPage() {
		return browserpage;
	}

	/**
	 * @param browserpage the browserpage to set
	 */
	public void setBrowserPage(browserpage browserpage) {
		this.browserpage = browserpage;
	}

}
